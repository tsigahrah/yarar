package yarar.tietokanta;

/**
 * Defined possible DB field types.<br>
 * <b>Feldtyp</b> comes from <a href="http://en.wikipedia.org/wiki/German_language"
 * target="_blank">German</a> and means <i>field type</i>.
 * 
 * @author Dimo Vanchev
 */
public enum Feldtyp {
    /** type Object */
    OBJECT,
    /** type String */
    STRING,
    /** type Array */
    ARRAY,
    /** type Boolean */
    BOOLEAN,
    /** type Integer */
    INTEGER
}
